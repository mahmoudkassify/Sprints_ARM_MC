/**********************************************************************************************************************
 *  LOCAL DATA 
 *********************************************************************************************************************/
static uint8 gu8_counter = 0;
/**********************************************************************************************************************
 *  GLOBAL DATA
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  LOCAL FUNCTION PROTOTYPES
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  LOCAL FUNCTIONS
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  GLOBAL FUNCTIONS
 *********************************************************************************************************************/
void CpuDriver_StartCriticalSection(void)
{
	gu8_counter++;
	
	if(1 >= gu8_counter)
	{
		CPUDRIVER_DISABLE_ALL_Interupts();
	}
	else
	{
		/*Already Disabled before*/
	}
}

void CpuDriver_StopCriticalSection(void)
{
	gu8_counter--;
	
	if(gu8_counter == 0)
	{
		CPUDRIVER_ENABLE_ALL_Interupts();
	}
	else
	{
		/*Should be zero to enable it*/
	}	
}

void CpuDriver_SoftwareReset(void)
{
    uint32 APINT_Temp = APINT;
    
    APINT_Temp &= ~((uint32)(CORTEXM4_APINT_VECTKEY_Msk | CORTEXM4_APINT_SW_RESET_Msk));
    
    APINT = (APINT_Temp                     |
             CORTEXM4_APINT_VECTKEY         |
             0x0004                         );
}


