#include "Common.h"
#include "WDTDriver.h"
#include "Timer.h"
#include "IntCtrl.h"
#include "McuDriver.h"

/**********************************************************************************************************************
*  LOCAL MACROS CONSTANT\FUNCTION
*********************************************************************************************************************/

/**********************************************************************************************************************
 *  LOCAL DATA 
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  GLOBAL DATA
 *********************************************************************************************************************/
extern const Wdg_ConfigType Wdg_Config;

extern const Gpt_ConfigType Gpt_Config[];

/**********************************************************************************************************************
 *  LOCAL FUNCTION PROTOTYPES
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  LOCAL FUNCTIONS
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  GLOBAL FUNCTIONS
 *********************************************************************************************************************/

int main(void)
{    
	CPUDRIVER_ENABLE_ALL_Interupts();
    IntCrtl_Init();
    
    Mcu_InitClock(Mcu_Clock_1);
    
    Wdg_Init(&Wdg_Config);
    
    Gpt_Init(Gpt_Config);
    
	for(;;)
    {
        static boolean b_first = TRUE;
        
        if(TRUE == b_first)
        {
            b_first = FALSE;
            
            Gpt_StartTimer(Gpt_ChannelType_16_32_TIMER0, 0x0FFF);
            Gpt_StartTimer(Gpt_ChannelType_32_64_WIDE_TIMER0, 0x0FFFFFFF);
        }
    }
}

